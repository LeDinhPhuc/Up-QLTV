    public boolean addNV(NhanVien nv) {               //Thêm sách vào database
        String sql = "INSERT INTO `quanlythuvien`.`nhanvien` (`MaNV_LDP`, `TenNV_LDP`,"
                + " `NgaySinh_LDP`, `GT_LDP`, `DiaChi_LDP`, `Email_LDP`, `SDT_LDP`) "
                + "VALUES (?,?,?,?,?,?,?);";
        try {
            ps = conn.prepareStatement(sql);
            ps.setString(1, nv.getMaNV());
            ps.setString(2, nv.getTenNV());
            ps.setString(3, nv.getNgaySinh());
            ps.setString(4, nv.getGT());
            ps.setString(5, nv.getDiaChi());
            ps.setString(6, nv.getEmail());
            ps.setString(7, nv.getSDT());

            int kt = ps.executeUpdate();
            if (kt != 0) {
                JOptionPane.showMessageDialog(null, "Bạn đã thêm dữ liệu thành công !");
                return true;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
        }
        return false;
    }

