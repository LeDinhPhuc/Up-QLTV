    public boolean update() {
        String sql = "UPDATE `quanlythuvien`.`nhanvien` "
                + "SET `MaNV_LDP` = ?, `TenNV_LDP` = ?, "
                + "`NgaySinh_LDP` = ?, `GT_LDP` = ?, "
                + "`DiaChi_LDP` = ?, `Email_LDP` = ?, "
                + "`SDT_LDP` = ? "
                + "WHERE (`MaNV_LDP` = ?);";
        try {
            ps = conn.prepareStatement(sql);
            ps.setString(1, maNV.getText());
            ps.setString(2, tenNV.getText());
            ps.setString(3, ngaySinh.getText());
            ps.setString(4, gT.getText());
            ps.setString(5, diaChi.getText());
            ps.setString(6, email.getText());
            ps.setString(7, sDT.getText());
            ps.setString(8, (Table1.getValueAt(Table1.getSelectedRow(), 0)).toString());

            int kt = ps.executeUpdate();
            if (kt != 0) {
                JOptionPane.showMessageDialog(null, "Bạn đã cập nhật dữ liệu thành công");
                //  Xóa dòng tại vị trí rowID lấy từ action mouse click
                model.removeRow(rowID);
                //  Thêm bản ghi vào bảng tại vị trí rowID lấy từ action event mouse click
                model.insertRow(rowID, new Object[]{
                    maNV.getText(), tenNV.getText(), ngaySinh.getText(), gT.getText(),
                    diaChi.getText(), email.getText(), sDT.getText()
                });
                clearData();
                return true;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
        }

        return false;
    }

